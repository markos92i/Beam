import Foundation

// MARK: - Main Macro

/// Transforms a protocol into a fully-functional API client struct.
///
/// Parameter roles are detected by **naming convention**:
/// - `body` (internal name) → JSON-encoded request body
/// - `query` (external name) → named query parameter
/// - Type `[URLQueryItem]` → query items array passed directly
/// - `header` (external name) → dynamic HTTP header
/// - Matches `{name}` in path → path parameter (string-interpolated)
///
/// Usage:
/// ```swift
/// @API(
///     host: URLs.api,
///     headers: APIConstants.headers,
///     client: Client(session: URLSessions.shared, certificates: APIConstants.certificates),
///     auth: AuthManager.shared,
///     crash: CrashManager.shared,
/// )
/// protocol AlertsAPI {
///     @Get("/alerts")
///     func list() async throws -> [AlertDto]
///
///     @Post("/alerts")
///     func create(body: AlertDto) async throws -> AlertDto
///
///     @Delete("/alerts/{id}")
///     func remove(id: Int) async throws
///
///     @Get("/offers")
///     func search(query page: Int, query limit: Int) async throws -> OffersDto
/// }
/// ```
@attached(peer, names: suffixed(Client))
public macro API(
    host: String,
    base: String = "",
    headers: [String: String] = [:],
    client: Any,
    auth: Any? = nil,
    crash: Any? = nil
) = #externalMacro(module: "BeamMacros", type: "APIMacro")

