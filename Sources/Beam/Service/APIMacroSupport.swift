// MARK: - Supporting Types

/// Task kind marker for route macros.
public enum _TaskKind: Sendable {
    case data
    case upload
    case download
}

// MARK: - Runtime Configuration Container

/// Stores the shared configuration generated by `@API`.
public struct _APIConfiguration: Sendable {
    public let host: String
    public let base: String
    public let components: [any RequestComponent]

    public init(host: String, base: String, components: [any RequestComponent]) {
        self.host = host
        self.base = base
        self.components = components
    }
}
